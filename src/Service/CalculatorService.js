"use strict";

/**
 * Method for sum
 * @author Saul Reyes Medina <saulreyesm@outlook.com>
 * @param val1 int
 * @param val2 int
 * @returns the sum of val1 + val2
 */
module.exports.sum = (val1, val2) => {
  return val1 + val2;
};

/**
 * Method for subtraction
 * @author Saul Reyes Medina <saulreyesm@outlook.com>
 * @param val1 - int
 * @param val2 - int
 * @returns the subtraction of val1 - val2
 */
module.exports.subtraction = (val1, val2) => {
  return val1 - val2;
};
